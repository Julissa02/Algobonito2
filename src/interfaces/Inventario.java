       public void reporteagregarpor(JTable tablaD,String t) {
        DefaultTableModel modeloTabla = new DefaultTableModel();
        tablaD.setModel(modeloTabla);
       
     modeloTabla.addColumn("ID");
       modeloTabla.addColumn("PRODUCTO");
       modeloTabla.addColumn("HORA");
       modeloTabla.addColumn("CANTIDAD");
  
      
    

        Object[] columna = new Object[4];
        int numReg=agdao.listaragregarporfecha(t).size();

        for (int i = 0; i < numReg; i++) {
       
           String aid_rpa=agdao.listaragregarporfecha(t).get(i).getId_ropa();
           
          
          columna[0] = agdao.listaragregarporfecha(t).get(i).getId_ropa();
          columna[1] = describropa(aid_rpa);
          columna[2] = agdao.listaragregarporfecha(t).get(i).getHora();
          columna[3] = agdao.listaragregarporfecha(t).get(i).getCantidad();
      
            modeloTabla.addRow(columna);
        }
        
     
    } 
    
